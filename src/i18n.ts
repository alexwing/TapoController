export function resolveLang(setting: string): Lang {
  if (setting === "es" || setting === "en") return setting;
  // "system": use the webview/OS locale.
  const sys = (navigator.language || "en").toLowerCase();
  return sys.startsWith("es") ? "es" : "en";
}

export function makeT(lang: Lang) {
  return (key: string): string => DICT[lang][key] ?? DICT.en[key] ?? key;
}
